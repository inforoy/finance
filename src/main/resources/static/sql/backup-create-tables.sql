CREATE TABLE IF NOT EXISTS T001_USUARIO (
  T001_ID_USUARIO VARCHAR(10) NOT NULL,
  T001_NOMBRES VARCHAR(30) NOT NULL,
  T001_APE_PATERNO VARCHAR(30)  NOT NULL,
  T001_APE_MATERNO VARCHAR(30)  NOT NULL,
  T001_USERNAME VARCHAR(11)  NOT NULL,
  T001_PASSWORDNAME VARCHAR(60)  NOT NULL,
  T001_ELIMINADO VARCHAR(2)  NOT NULL,
  PRIMARY KEY (T001_ID_USUARIO)
);

CREATE TABLE IF NOT EXISTS T002_PERSONA (
  T002_DNI_NUMERO VARCHAR(8) NOT NULL,
  T002_NOMBRES VARCHAR(30) NOT NULL,
  T002_APE_PATERNO VARCHAR(30),
  T002_APE_MATERNO VARCHAR(30),
  T002_RUC_NUMERO VARCHAR(11),
  T002_RAZON_SOCIAL VARCHAR(60),
  T002_DIRECCION VARCHAR(60),
  T002_SEXO VARCHAR(9) NOT NULL,
  T002_FECHA_NACIMIENTO DATE,
  T002_EMAIL VARCHAR(35),
  T002_TELEFONO_MOVIL VARCHAR(20),
  T002_TELEFONO_FIJO VARCHAR(20),
  T002_ESTADO_CIVIL VARCHAR(20),
  T002_DEPARTAMENTO VARCHAR(20),
  T002_PROVINCIA VARCHAR(20),
  T002_DISTRITO VARCHAR(20),
  T002_ELIMINADO VARCHAR(2),
  PRIMARY KEY (T002_DNI_NUMERO)
);